userInFst :: Relacion -> Usuario -> Bool
userInFst rel u
    | idDeUsuario u == idDeUsuario (fst rel) = True
    | otherwise = False

userInSnd :: Relacion -> Usuario -> Bool
userInSnd rel u
    | idDeUsuario u == idDeUsuario (snd rel) = True
    | otherwise = False    

